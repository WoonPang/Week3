using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

// 스택 메소드
void Push(object)       // 스택의 최상위에 새로운 요소 추가
object Peek()       // 스택의 최상위 요소를 삭제하지 않고, 요소를 반환할 때 사용
object Pop()        // 스택의 최상위 요소를 삭제하고, 삭제한 요소를 반환할 때 사용
void Clear()        // 스택의 모든 요소를 삭제
int Count       // 현재 스택에 저장된 요소의 개수