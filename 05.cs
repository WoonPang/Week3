using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

// 큐 메소드
void Enqueue(object)        // 후단에 새로운 요소 추가
object Peek()       // 전단에 있는 요소를 삭제하지 않고 요소를 반활할 때 사용
object Dequeue()        // 전단에 있는 요소를 삭제하고, 삭제한 요소를 반환
void Clear()        // 큐의 모든 요소를 삭제
int Count       // 현재 큐에 저장된 요소의 개수